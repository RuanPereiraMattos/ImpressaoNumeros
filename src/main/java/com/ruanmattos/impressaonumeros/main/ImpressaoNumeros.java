package com.ruanmattos.impressaonumeros.main;

/**
 * Impressão dos Numeros de 1 a 10
 *
 * @author Ruan Pereira Mattos
 */
public class ImpressaoNumeros {

    public static void main(String[] args) {
        System.out.println("Bem vindo(a) ao programa que irá imprimir na tela do número 1 ao número 10");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
